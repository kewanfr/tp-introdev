package suitecond

/*
On considère la suite U suivante :
- U(0) = 5
- U(n) = U(n-1) - 2 si n est pair
- U(n) = U(n-1) * 3 si n est impair
La fonction u calcule les termes de cette suite.

# Entrée
- n : le numéro du terme à calculer

# Sortie
- un : la valeur de U(n)

# Info
2024-2025, test 1, exercice 4
*/

func u(n int) (un int) {

	if (n == 0) {
		return 5
	}

	if n % 2 == 0 {
		return u(n-1) - 2
	}else {
		return u(n-1)* 3
	}
}
